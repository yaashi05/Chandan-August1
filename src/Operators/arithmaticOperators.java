package Operators;

public class arithmaticOperators {

	public static void main(String[] args) {

	    int a = 28, b = 14;

	    System.out.println("a + b = " + (a + b));

	    System.out.println("a - b = " + (a - b));

	    System.out.println("a * b = " + (a * b));

	    System.out.println("a / b = " + (a / b));

	    System.out.println("a % b = " + (a % b));
	  }
	}
	