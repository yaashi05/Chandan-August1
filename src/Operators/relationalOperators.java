package Operators;

public class relationalOperators {

	public static void main(String[] args) {
	    int a = 24, b = 48;

	    System.out.println("a is " + a + " and b is " + b);

	    System.out.println(a == b);  

	    System.out.println(a != b);  

	    System.out.println(a > b);  

	    System.out.println(a < b);  

	    System.out.println(a >= b);  

	    System.out.println(a <= b); 
	}

}
